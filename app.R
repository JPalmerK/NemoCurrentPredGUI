library(shiny)
library(ncdf4) # package for netcdf manipulation
library(raster) # package for raster manipulation
library(rgdal) # package for geospatial analysis
library(ggplot2) # package for plotting


# Define UI for application that draws a histogram
ui <- fluidPage(

    # Application title
    titlePanel("Old Faithful Geyser Data"),

    # Sidebar with a slider input for number of bins 
    # Application title
    titlePanel("Export Tide Predictions"),
    
    #  Sidebar with a slider input for number of maps
    sidebarLayout(
        sidebarPanel(
            actionButton('outLoc', 'Export Data as CSV'),
            actionButton('EffortData', 'Export Effort Data')),
        
        # Display a dynamically created set of maps
        mainPanel(
            uiOutput("tiledMaps")
        )
    )
)

# Define server logic required to draw a histogram
server <- function(input, output) {

    output$distPlot <- renderPlot({
        # generate bins based on input$bins from ui.R
        x    <- faithful[, 2]
        bins <- seq(min(x), max(x), length.out = input$bins + 1)

        # draw the histogram with the specified number of bins
        hist(x, breaks = bins, col = 'darkgray', border = 'white')
    })
}

# Run the application 
shinyApp(ui = ui, server = server)
